                    <h4>The Assessment Process</h4>
                    <p>The assessment will take the form of an examination of the doctor’s physical or mental health by a registered Psychiatrist or other relevant Specialist. If a doctor is willing to cooperate, s/he chooses a Psychiatrist/ other Specialist of their choice. The Psychiatrist/ other Specialist will then submit a detailed report to the Committee. Following an interview with the alleged impaired practitioner, the Committee will then determine the practitioner’s fitness to practice. Depending on the gravity of the illness some practitioners are suspended from practising whilst others are allowed to practice under surveillance.</p>
